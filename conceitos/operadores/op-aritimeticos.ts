// Usados para realizar cálculos matemáticos
// Operadores: + (adição), - (subtração), * (multiplicação), / (divisão), % (módulo)

const a: number = 10
const b: number = 5
const c: number = 15

// ======================================
// OPERAÇÕES BÁSICAS
// ======================================

console.log("Adição: a + b =", a + b)          // 15
console.log("Subtração: a - b =", a - b)       // 5
console.log("Multiplicação: a * b =", a * b)   // 50
console.log("Divisão: a / b =", a / b)         // 2
console.log("Módulo: a % b =", a % b)          // 0 (resto da divisão)

// ======================================
// EXPRESSÃO COM PRECEDÊNCIA
// ======================================

// Parênteses definem a ordem das operações (igual à matemática)
const resultado = (a + c) / b
console.log("Resultado de (a + c) / b =", resultado) // 5

