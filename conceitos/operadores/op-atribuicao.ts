// ======================================
// OPERADORES DE ATRIBUIÇÃO
// ======================================

// Usados para atribuir ou atualizar o valor de uma variável
// Operadores: =, +=, -=, *=, ++, --

let contador: number = 0

// ======================================
// INCREMENTO (aumentar valor)
// ======================================

contador = contador + 1  // forma explícita
contador += 1            // forma abreviada (equivalente ao de cima)
contador++               // incremento unitário (adiciona 1)
console.log("Incremento:", contador)

// ======================================
// DECREMENTO (diminuir valor)
// ======================================

contador -= 1            // subtrai 1 e atribui
contador--               // decremento unitário (diminui 1)
console.log("Decremento:", contador)

// ======================================
// MULTIPLICAÇÃO E ATRIBUIÇÃO
// ======================================

contador *= 2            // multiplica por 2 e atribui
console.log("Multiplicação:", contador)

