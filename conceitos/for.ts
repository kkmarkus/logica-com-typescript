// ======================================
// LAÇO DE REPETIÇÃO: FOR
// ======================================

// Sintaxe: for(inicialização; condição; incremento/decremento)
// Usado quando sabemos quantas vezes queremos repetir

// ======================================
// FOR CRESCENTE (0 até 10)
// ======================================

for (let i = 0; i <= 10; i++) {
  console.log(i)
}

// ======================================
// FOR DECRESCENTE (10 até 0)
// ======================================

for (let j = 10; j >= 0; j--) {
  console.log(j)
}

