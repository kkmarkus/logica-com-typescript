// ======================================
// TIPOS DE DECLARAÇÃO DE VARIÁVEIS
// ======================================

// var -> forma antiga (evite usar)
var cidade = "Teresina";

// let -> variável que pode mudar
let idade = 25;
idade = 26;

// const -> valor constante (não pode ser reatribuído)
const nome = "João";

// ======================================
// TIPOS BÁSICOS DE VARIÁVEIS
// ======================================

// String (texto)
let texto = "Olá, Node.js!";

// Number (números)
let inteiro = 10;
let decimal = 5.75;

// Boolean (verdadeiro ou falso)
let ligado = true;
let ativo = false;

// Null (valor vazio intencional)
let vazio = null;

// Undefined (sem valor definido)
let indefinido;

// Array (lista)
let frutas = ["maçã", "banana", "uva"];

// Object (objeto)
let pessoa = {
  nome: "Maria",
  idade: 30,
  profissao: "Desenvolvedora"
};
